const double = (n) => n * 2;

console.log(double(4)); // Output: 8


// Function with Multiple Parameters

const sayHi = () => console.log("Hi!");

sayHi(); // Output: Hi!

const multiply = (x, y) => x * y;

console.log(multiply(3, 4)); // Output: 12

