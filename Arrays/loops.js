
/* ---------- For of ---------- */


const fruits = ["apple", "banana", "mango"];

for (const fruit of fruits) {
  console.log(fruit);
}

// Output:

// → apple
// → banana
// → mango

/* ---------- For each ---------- */
const numbers = [1, 2, 3, 4, 5];

numbers.forEach((number) => {
  console.log(number);
});

// Output:

// → 1
// → 2
// → 3
// → 4
// → 5

